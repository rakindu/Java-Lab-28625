public class CrouchingPlayer extends Player {
    public void moveUp() {
        System.out.println("Player moved 2 spaces up while crouching.");
    }


    public void moveDown() {
        System.out.println("Player moved 2 spaces down while crouching.");
    }


    public void moveLeft() {
        System.out.println("Player moved 2 spaces left while crouching.");
    }


    public void moveRight() {
        System.out.println("Player moved 2 spaces right while crouching.");
    }
}
