public class JumpingPlayer extends Player{
    public void moveUp() {
        System.out.println("Player jumped 5 spaces up.");
    }


    public void moveDown() {
        System.out.println("Player jumped 5 spaces down.");
    }


    public void moveLeft() {
        System.out.println("Player jumped 5 spaces left.");
    }


    public void moveRight() {
        System.out.println("Player jumped 5 spaces right.");
    }
}
